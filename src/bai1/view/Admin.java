    public Admin() {
        initComponents();
        model = (DefaultTableModel) tableSkill.getModel();
        BufferedReader br = null;
        ArrayList<String> listString = new ArrayList<>();
        try{
            br = new BufferedReader(new FileReader(filePath));
            String skillRead;
            while((skillRead = br.readLine()) != null){
                listString.add(skillRead);
            }
            br.close();
        }catch(Exception ex){
            ex.printStackTrace();
        }
        for(String s: listString){
            StringTokenizer stk = new StringTokenizer(s," ");
            int sid = Integer.parseInt(stk.nextToken());
            String sname = stk.nextToken();
            listSkill.add(new Skill(sid, sname));
        }
        addToTable();
    }
    
    private void addToTable(){
        model.setRowCount(0);
        for(Skill skill : listSkill){
            model.addRow(skill.toObjects());
        }
    }
    
    private int getSelected(){
        return tableSkill.getSelectedRow();
    }

