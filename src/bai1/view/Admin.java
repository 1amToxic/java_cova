    public void updateSkill(){
        int id = listSkill.get(getSelected()).getId();
        listSkill.remove(getSelected());
        listSkill.add(getSelected(), new Skill(id, update.getContent()));
        addToTable();
    }
    public void addSkill(String s){
        listSkill.add(new Skill(listSkill.get(listSkill.size()-1).getId()+1, s));
        addToTable();
    }
    public void updateToFile(){
        BufferedWriter bw = null;
        try{
            bw = new BufferedWriter(new FileWriter(filePath));
            for(Skill skill : listSkill){
                bw.write(skill.toString());
            }
            bw.close();
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
    
